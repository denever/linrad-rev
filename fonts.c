void settextcolor(unsigned char color)
{
unsigned char *dp;
int i;
i=text_width*text_height*160;
dp=(void*)vga_font;    
while (i > 0) 
  {
  if (*dp) *dp = color;
  dp++;
  i--;
  }
}

void expandfont( void *_f1)
{
// Convert bit-per-pixel font to byte(s)-per-pixel font 
unsigned char *f1;
unsigned char *f2;
int i, x, y, b;
b=0;
f1 = _f1;
f2 = (void*)vga_font;
for(i=0; i<160; i++) 
  {
  for(y=0; y<font_h; y++)
    {
    for(x=0; x<font_w; x++)
      {
      if(x%8 == 0) b=*f1++;
      if(b & ( 128 >> (x%8)) )
        {
        *f2 = 7;
        } 
      else
        {
        *f2 = 0;
        }
      f2++;
      }
    }
  }
for(i=160; i<256; i++) 
  {
  for(y=0; y<font_h; y++)
    {
    for(x=0; x<font_w; x++)
      {
      *f2 = 0;
      f2++;
      }
    }
  }
}

void init_font(int font_type)
{
int scale;
unsigned char *dp;
int i,j,k,m,n;
int fh, fw;
int mm,nn;
// Fonts are bitmaps. gl_write(x,y,"Z"); is equivalent to this:
// gl_putbox(x,y,fw,fh,&vga_font['Z'*fw*fh]);
vga_font=chk_free(vga_font);
scale=1;
dp=NULL;
fw=0;
fh=0;
switch (font_type)
  {
  case 1:
  dp=font8x8;
  fh=8;
  fw=8;
  font_xsep=0;
  font_ysep=2;
  break;
  
  case 2:
  dp=font8x12;
  fh=12;
  fw=8;
  font_xsep=0;
  font_ysep=2;
  break;
  
  case 3:
  dp=font8x13;
  fh=13;
  fw=8;
  font_xsep=1;
  font_ysep=2;
  break;
  
  case 4:
  dp=font8x8;
  fh=8;
  fw=8;
  scale=2;
  font_xsep=0;
  font_ysep=4;
  break;
  
  default:
  dp=font8x12;
  fh=12;
  fw=8;
  scale=2;
  font_xsep=0;
  font_ysep=2;
  break;
  }
font_w=fw*scale;
font_h=fh*scale;
font_size=font_w*font_h;  
vga_font = malloc(256*font_size);
if(vga_font == NULL)
  {
  lir_errcod=1035;
  return;
  }
expandfont(dp);
if(scale >1)
  {
  for(i=255; i>=0; i--)
    {
    for(j=0; j<fh; j++)
      {
      m=j*scale;
      for(k=0; k<fw; k++)
        {
        n=k*scale;
        for(nn=0; nn<scale; nn++)        
          {
          for(mm=0; mm<scale; mm++)        
            {
            vga_font[i*font_size+n+nn+(m+mm)*font_w] =
                                        vga_font[i*fw*fh+k+j*fw];                              
            }
          }  
        }
      }  
    }
  }
text_width=font_w+font_xsep;
text_height=font_h+font_ysep;  
screen_last_line=(screen_height-1)/text_height-1;
screen_last_xpixel=screen_width-1;
screen_last_col=screen_width/text_width-1;
}
